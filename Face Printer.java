//Face Printer


public class FacePrinter {
    public static void main(String[] args) {
       String[] face = {
            " +\"\"\"\"\"+",
            "[| o o |]",
            " |  ^  |",
            " | '-' |",
            " +-----+"
        };

        for (String line : face) {
            System.out.println(line);
        }
    }
}
